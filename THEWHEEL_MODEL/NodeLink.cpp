//////////////////////////////////////////////////////////////////////
// CNodeLink::CNodeLink
// 
// constructs a CNodeLink to the given target, with the given weight
//////////////////////////////////////////////////////////////////////
CNodeLink::CNodeLink(CNode *pToNode, REAL weight)
	: m_pTarget(pToNode),
		m_weight(weight),
		m_gain((REAL) 1.0),
		m_bIsStabilizer(FALSE),
		m_bHasPropagated(TRUE)
		// initialize to HasPropagated to prevent immediate 
		//		propagation
{
}	// CNodeLink::CNodeLink


//////////////////////////////////////////////////////////////////////
// CNodeLink::~CNodeLink
// 
// destroys this node link
//////////////////////////////////////////////////////////////////////
CNodeLink::~CNodeLink()
{
}	// CNodeLink::~CNodeLink


//////////////////////////////////////////////////////////////////////
// implements CNodeLink's dynamic serialization
//////////////////////////////////////////////////////////////////////
IMPLEMENT_SERIAL(CNodeLink, CObject, VERSIONABLE_SCHEMA|2);
//		2 -- added stabilizer flag
//		1 -- initial version

//////////////////////////////////////////////////////////////////////
// CNodeLink::GetWeight
// 
// returns the weight of this link
//////////////////////////////////////////////////////////////////////
REAL CNodeLink::GetWeight() const
{
	return m_weight;

}	// CNodeLink::GetWeight


//////////////////////////////////////////////////////////////////////
// CNodeLink::SetWeight
// 
// sets the weight of this link
//////////////////////////////////////////////////////////////////////
void CNodeLink::SetWeight(REAL newWeight)
{
	m_weight = newWeight;

}	// CNodeLink::SetWeight


//////////////////////////////////////////////////////////////////////
// CNodeLink::GetTarget
// 
// returns the target of this link
//////////////////////////////////////////////////////////////////////
CNode *CNodeLink::GetTarget()
{
	return m_pTarget;

}	// CNodeLink::GetTarget


//////////////////////////////////////////////////////////////////////
// CNodeLink::GetTarget const
// 
// returns the target of this link as a const pointer
//////////////////////////////////////////////////////////////////////
const CNode *CNodeLink::GetTarget() const
{
	return m_pTarget;

}	// CNodeLink::GetTarget const


//////////////////////////////////////////////////////////////////////
// CNodeLink::SetTarget
// 
// sets the target of this link
//////////////////////////////////////////////////////////////////////
void CNodeLink::SetTarget(CNode *pTarget)
{
	m_pTarget = pTarget;

}	// CNodeLink::SetTarget


//////////////////////////////////////////////////////////////////////
// CNodeLink::HasPropagated
// 
// flag to indicate that propagation has occurred through the link
//////////////////////////////////////////////////////////////////////
BOOL CNodeLink::HasPropagated() const
{
	return m_bHasPropagated;

}	// CNodeLink::HasPropagated


//////////////////////////////////////////////////////////////////////
// CNodeLink::SetHasPropagated
// 
// sets/clears flag to indicate propagation
//////////////////////////////////////////////////////////////////////
void CNodeLink::SetHasPropagated(BOOL bPropagated)
{
	m_bHasPropagated = bPropagated;

}	// CNodeLink::SetHasPropagated


//////////////////////////////////////////////////////////////////////
// CNodeLink::Serialize
// 
// serializes the node link
//////////////////////////////////////////////////////////////////////
void CNodeLink::Serialize(CArchive &ar)
{
	UINT nSchema = ar.GetObjectSchema();

	if (ar.IsLoading())
	{
		// load the values
		float weight;
		ar >> weight;
		m_weight = (REAL) weight;
		ar >> m_pTarget;

		if (nSchema >= 2)
		{
			ar >> m_bIsStabilizer;
		}
	}
	else
	{
		// store the values
		ar << (float) m_weight;
		ar << m_pTarget;

		if (nSchema >= 2)
		{
			ar << m_bIsStabilizer;
		}
	}

}	// CNodeLink::Serialize

void CNodeLink::SetGain(REAL gain)
{
	ASSERT(_finite(m_gain));
	m_gain = 0.5 * m_gain + 0.5 * gain;
	ASSERT(_finite(m_gain));
}

REAL CNodeLink::GetGainWeight() const
{
	return 0.99 * m_gain * GetWeight() + 0.01 * GetWeight();
}


//////////////////////////////////////////////////////////////////////
// CNodeLink::IsStabilizer
// 
// returns TRUE if this link is a stabilizer link
//////////////////////////////////////////////////////////////////////
BOOL CNodeLink::IsStabilizer() const
{
	return m_bIsStabilizer;

}	// CNode::IsStabilizer


//////////////////////////////////////////////////////////////////////
// CNodeLink::SetStabilizer
// 
// links the node to the target node (creating a CNodeLink in the
// proces, if necessary).
//////////////////////////////////////////////////////////////////////
void CNodeLink::SetStabilizer(BOOL bIsStabilizer)
{
	m_bIsStabilizer = bIsStabilizer;
}

